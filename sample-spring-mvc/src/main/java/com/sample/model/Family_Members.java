package com.sample.model;

public class Family_Members {

	private int f_id;
	private String f_name;
	private String f_weight;
	private String f_profession;
	public int getF_id() {
		return f_id;
	}
	public void setF_id(int f_id) {
		this.f_id = f_id;
	}
	public String getF_name() {
		return f_name;
	}
	public void setF_name(String f_name) {
		this.f_name = f_name;
	}
	public String getF_weight() {
		return f_weight;
	}
	public void setF_weight(String f_weight) {
		this.f_weight = f_weight;
	}
	public String getF_profession() {
		return f_profession;
	}
	public void setF_profession(String f_profession) {
		this.f_profession = f_profession;
	}
	
	
}
